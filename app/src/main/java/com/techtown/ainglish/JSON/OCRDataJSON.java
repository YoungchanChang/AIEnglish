package com.techtown.ainglish.JSON;

public class OCRDataJSON {

    private String user_info;

    private String img_path;

    private String ocr_data;

    private String ocr_create_time;

    public String getUser_info() {
        return user_info;
    }

    public void setUser_info(String user_info) {
        this.user_info = user_info;
    }

    public String getImg_path() {
        return img_path;
    }

    public void setImg_path(String img_path) {
        this.img_path = img_path;
    }

    public String getOcr_data() {
        return ocr_data;
    }

    public void setOcr_data(String ocr_data) {
        this.ocr_data = ocr_data;
    }

    public String getOcr_create_time() {
        return ocr_create_time;
    }

    public void setOcr_create_time(String ocr_create_time) {
        this.ocr_create_time = ocr_create_time;
    }
}
